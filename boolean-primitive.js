function booWho(bool) {
  if (bool === true || bool === false) {
    return true;
  }
  return false;
}

booWho(null);

// second solution
function booWho(bool) {
  if (typeof bool === 'boolean') {
    return true;
  }
  return false;
}

booWho(null);

